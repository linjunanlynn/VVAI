/** 条内直接可点的指令（前 N 条） */
export function getDockBarInlineShortcuts(appId: string): string[] {
  const list = DOCK_APP_SHORTCUTS[appId]
  if (!list?.length) return []
  return [...list].slice(0, DOCK_INLINE_SHORTCUT_LIMIT)
}

/** 「智能指令推荐」抽屉内全部可发送指令（含条内已展示的，便于用户再次选用） */
export function getDockSuggestedPrompts(appId: string): string[] {
  const list = DOCK_APP_SHORTCUTS[appId]
  return list ? [...list] : []
}

/** @deprecated 使用 getDockBarInlineShortcuts / getDockSuggestedPrompts */
export function getDockAppShortcuts(appId: string): string[] {
  return getDockSuggestedPrompts(appId)
}
